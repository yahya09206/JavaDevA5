package day19_forloops;

public class Shape2 {
    public static void main(String[] args) {

        System.out.println("\t\t* * * * * * * *");
        for (int i = 1; i <= 6; i++){
            System.out.println("\t\t*             *");
        }
        System.out.println("\t\t* * * * * * * *");
    }
}
