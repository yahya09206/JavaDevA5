package day19_forloops;

public class ForLoopIntro {
    public static void main(String[] args) {

        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");

        System.out.println("------------------------------------");

        for (int i = 0; i <= 5; i++){
            System.out.println("Hello World!");
        }
    }
}
