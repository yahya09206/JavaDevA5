package day23_arrays;

public class ArraysIntro {
    public static void main(String[] args) {

        // Declare Array
        String[] group1 = {"Feruza", "Yuliang", "Gulistan", "Aykut"};

        // loop through array
        for (int i = 0; i <= group1.length - 1; i++){
            System.out.println(group1[i]);
        }
    }
}
