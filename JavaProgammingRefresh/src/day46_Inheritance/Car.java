package day46_Inheritance;

public class Car {

    private String brand;
    private String model;
    private int year;
    private String color;
    private double miles;

    public Car(String brand, String model, int year, String color, double miles) {
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.color = color;
        this.miles = miles;
    }


}
